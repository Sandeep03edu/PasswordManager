fun getCardIssuerNameList(): MutableList<String> {
    val pairList = getCardIssuerNameLogoPair()

    val list : MutableList<String> = mutableListOf()
    pairList.forEach {
        list.add(it.first)
    }

    return list
}

fun getCardIssuerLogo(name: String) : ImageResource{
    val pairList = getCardIssuerNameLogoPair()

    pairList.forEach {
        if(it.first==name){
            return it.second
